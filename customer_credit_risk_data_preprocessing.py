df_transformed = preprocessor.fit_transform(df_csv)

# Column names generate karo
ohe_cols = preprocessor.named_transformers_['cat'].get_feature_names_out(cat_cols)

all_cols = list(num_cols) + list(ohe_cols)

# DataFrame ma convert karo
df_final = pd.DataFrame(df_transformed, columns=all_cols)

