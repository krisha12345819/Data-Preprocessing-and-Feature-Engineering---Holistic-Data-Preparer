"""#### III. Robust Scaling

Robust scaling uses median and IQR (Interquartile Range).

It is resistant to outliers and works well when data contains extreme values.
"""

robust = RobustScaler()

df_csv[['annual_income_robust', 'loan_amount_robust']] = robust.fit_transform(
    df_csv[['annual_income', 'loan_amount']]
)

