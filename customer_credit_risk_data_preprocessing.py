"""## 10. Encoding Numerical Features

Numerical feature encoding is the process of converting continuous numerical values into discrete categories or groups.

This helps:
- Handle outliers
- Capture non-linear relationships
- Improve model interpretability

### 1. Binning:
   Continuous values are divided into groups (bins) to simplify data and reduce noise.
"""

df_csv['income_bin'] = pd.cut(
    df_csv['annual_income'],
    bins=3,
    labels=['Low', 'Medium', 'High']
)

