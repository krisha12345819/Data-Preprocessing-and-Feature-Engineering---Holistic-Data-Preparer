"""### 2. Normalization

Normalization scales values to unit norm (L1 or L2).

It is useful when direction of data matters more than magnitude.

#### I. Min-Max Scaling

Min-Max scaling transforms data into a fixed range [0,1].

It preserves the shape of distribution but rescales values.
"""

minmax = MinMaxScaler()

df_csv[['annual_income_minmax', 'loan_amount_minmax']] = minmax.fit_transform(
    df_csv[['annual_income', 'loan_amount']]
)

