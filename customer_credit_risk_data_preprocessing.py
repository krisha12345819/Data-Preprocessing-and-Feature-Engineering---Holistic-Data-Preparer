"""# Part G: Feature Construction & Transformation

## 12. Transformations:

### I. FunctionTransformer

FunctionTransformer is used to apply mathematical transformations like:
- Log
- Reciprocal
- Square Root

Applied on:
- spending_ratio
"""

# Log Transform
log_transform = FunctionTransformer(np.log1p)
df_csv['spending_log'] = log_transform.fit_transform(df_csv[['spending_ratio']])

# Square Root Transform
sqrt_transform = FunctionTransformer(np.sqrt)
df_csv['spending_sqrt'] = sqrt_transform.fit_transform(df_csv[['spending_ratio']])

# Reciprocal Transform
df_csv['spending_reciprocal'] = 1 / (df_csv['spending_ratio'] + 1)

print(df_csv[['spending_ratio', 'spending_log', 'spending_sqrt']].head())

"""### II. PowerTransformer

PowerTransformer is used to make data more normally distributed.

Methods:
- Box-Cox (only positive data)
- Yeo-Johnson (works with negative values)

Applied on:
- loan_amount
- annual_income
"""

# Box-Cox (only for positive values)

pt_boxcox = PowerTransformer(method='box-cox')

df_csv[['loan_boxcox', 'income_boxcox']] = pt_boxcox.fit_transform(
    df_csv[['loan_amount', 'annual_income']]
)

print(df_csv[['loan_amount', 'loan_boxcox']].head())

# Yeo-Johnson (safe for all values)
pt = PowerTransformer(method='yeo-johnson')

df_csv[['loan_power', 'income_power']] = pt.fit_transform(
    df_csv[['loan_amount', 'annual_income']]
)

print(df_csv[['loan_amount', 'loan_power']].head())

