df_csv['credit_binary'] = binarizer.fit_transform(
    df_csv[['credit_score']]
)

print(df_csv[['credit_score', 'credit_binary']].head())

"""### 3. Quantile Binning:
   Divides data into equal-sized groups (equal frequency bins).
"""

df_csv['transaction_quantile'] = pd.qcut(
    df_csv['transaction_count'],
    q=4,
    labels=['Q1', 'Q2', 'Q3', 'Q4']
)

