"""# Part D: Outlier Handling Techniques

Outliers are extreme values that differ significantly from other observations.  
 They can affect model performance, so handling them is important.

## 1. Z-score Method:
   Detects outliers based on standard deviation.
"""

z = np.abs(stats.zscore(df_csv[['age', 'annual_income', 'loan_amount', 'credit_score']]))

df_z = df_csv[(z < 3).all(axis=1)]

print("Shape after Z-score:", df_z.shape)

"""## 2. IQR Method:
   Uses quartiles to identify extreme values.
"""

Q1 = df_csv[['age','annual_income','loan_amount','credit_score']].quantile(0.25)
Q3 = df_csv[['age','annual_income','loan_amount','credit_score']].quantile(0.75)

IQR = Q3 - Q1

df_iqr = df_csv[~((df_csv[['age','annual_income','loan_amount','credit_score']] < (Q1 - 1.5 * IQR)) |
                  (df_csv[['age','annual_income','loan_amount','credit_score']] > (Q3 + 1.5 * IQR))).any(axis=1)]

print("Shape after IQR:", df_iqr.shape)

"""## 3. Percentile Method:
   Removes values outside a specific percentile range.
"""

lower = df_csv[['annual_income']].quantile(0.01)
upper = df_csv[['annual_income']].quantile(0.99)

df_percentile = df_csv[(df_csv['annual_income'] >= lower.values[0]) &
                       (df_csv['annual_income'] <= upper.values[0])]

print("Shape after Percentile:", df_percentile.shape)

"""## 4. Winsorization:
   Caps extreme values instead of removing them.
"""

df_csv['annual_income_winsor'] = winsorize(df_csv['annual_income'], limits=[0.01, 0.01])

print(df_csv[['annual_income', 'annual_income_winsor']].head())

